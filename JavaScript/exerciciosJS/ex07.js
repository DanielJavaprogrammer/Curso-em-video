var valor1 = true;
var valor2 = true;

if (valor1 === true && valor2 === true) {
    console.log("Ambos os valores são VERDADEIROS.");
} else if (valor1 === false && valor2 === false) {
    console.log("Ambos os valores são FALSOS.");
} else {
    console.log("Os valores são diferentes.");
}
